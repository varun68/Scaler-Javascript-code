// inbuilt js array methods

var arr=[12,14,16,18,20]


//pop method remove element from last
arr.pop()//it will take out last element from the array
console.log('popped array:',arr)



//push method add element at last

var c=arr.push(100)
console.log(c)

console.log('pushed array:',arr)


//shift remove element from starting

var d=arr.shift()
console.log(d)

console.log('shifted array:',arr)


//unshift add element from starting
arr.unshift(102)
console.log(arr)
